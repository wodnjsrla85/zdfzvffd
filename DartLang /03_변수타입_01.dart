main() {
  //변수 타입 선언자
  //정수 
  int intNum1 = 30;
  print(intNum1);

  int intNum2 = 20;
  print(intNum2);

  //사칙연산
  print(" ** 정수 사칙 연산 **");
  print(intNum1 + intNum2); //덧셈
  print(intNum1 - intNum2); //뺄셈
  print(intNum1 * intNum2); //곱셈
  print(intNum1 / intNum2); //나눗셈
  print(intNum1 % intNum2); //나머지
  print(intNum1 ~/ intNum2); //몫

  //실수
  double doubleNum1 = 1.5;
  double doubleNum2 = 0.2;

  print(doubleNum1 + doubleNum2);
  print(doubleNum1 - doubleNum2);
  print(doubleNum1 / doubleNum2);
  print(doubleNum1 * doubleNum2);
  print(doubleNum1 % doubleNum2);
  print(doubleNum1 ~/ doubleNum2);

  
}
